#!/usr/bin/env node
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const args = parseArgs(process.argv.slice(2));
if (args.help) {
  printHelp();
  process.exit(0);
}

const upstream = args.upstream;
if (!upstream) {
  throw new Error("--upstream is required");
}

const manifestPath = args.manifest ?? "config/custom-fonts.json";
const reportPath = args.report;
const manifest = JSON.parse(await readFile(manifestPath, "utf8"));
const report = {
  patchedFonts: 0,
  localOnly: [],
};

validateManifest(manifest);

const fontList = manifest.fonts.map((font, index) => ({
  ...font,
  fontValue: manifest.uiBaseId + index,
}));

const existingKeys = await readExistingFontFamilyKeys(upstream);
const skipped = [];
const toAdd = [];
for (const font of fontList) {
  if (existingKeys.has(font.label)) {
    skipped.push(font);
  } else {
    toAdd.push(font);
  }
}
if (skipped.length > 0) {
  console.warn(
    `Skipping ${skipped.length} font(s) already built into upstream FONT_FAMILY: ${skipped.map((font) => font.label).join(", ")}`,
  );
}

await patchConstants(upstream, toAdd);
await patchMetadata(upstream, toAdd);
await writeCustomFontCss(upstream, toAdd);

report.patchedFonts = toAdd.length;
report.skippedBuiltIn = skipped.map((font) => font.label);
report.localOnly = toAdd.map((font) => font.label);

if (reportPath) {
  await mkdir(path.dirname(reportPath), { recursive: true });
  await writeFile(reportPath, `${JSON.stringify(report, null, 2)}\n`);
}

console.log(JSON.stringify(report, null, 2));

async function patchConstants(root, fonts) {
  const constantsPath = path.join(root, "packages/common/src/constants.ts");
  let text = await readFile(constantsPath, "utf8");

  const familyEntries = fonts
    .map((font) => `  ${JSON.stringify(font.label)}: ${font.fontValue},`)
    .join("\n");

  text = insertObjectBlock(
    text,
    "export const FONT_FAMILY",
    "PROJECT_CUSTOM_FONTS",
    familyEntries,
  );

  const fallbackEntries = fonts
    .map((font) => {
      const target =
        font.fallback === "monospace"
          ? "MONOSPACE_GENERIC_FONT"
          : "SANS_SERIF_GENERIC_FONT";
      return `    case FONT_FAMILY[${JSON.stringify(font.label)}]:\n      return ${target};`;
    })
    .join("\n");

  text = insertBeforeDefaultCase(
    text,
    "PROJECT_CUSTOM_FONT_FALLBACKS",
    fallbackEntries,
  );

  await writeFile(constantsPath, text);
}

async function patchMetadata(root, fonts) {
  const metadataPath = path.join(root, "packages/common/src/font-metadata.ts");
  let text = await readFile(metadataPath, "utf8");
  const metadataEntries = fonts
    .map(
      (font) =>
        `  [FONT_FAMILY[${JSON.stringify(font.label)}]]: { metrics: { unitsPerEm: 1000, ascender: 886, descender: -374, lineHeight: 1.25 } },`,
    )
    .join("\n");

  text = insertObjectBlock(
    text,
    "export const FONT_METADATA",
    "PROJECT_CUSTOM_FONT_METADATA",
    metadataEntries,
  );

  await writeFile(metadataPath, text);
}

async function writeCustomFontCss(root, fonts) {
  const cssBlocks = fonts.map((font) => {
    const localNames = Array.from(new Set([font.label, ...(font.localNames ?? [])]));
    const sources = localNames.map((name) => `local(${quoteCss(name)})`);
    return `@font-face {
  font-family: ${quoteCss(font.label)};
  src: ${sources.join(", ")};
  font-display: swap;
}`;
  });

  const cssPath = path.join(root, "excalidraw-app/custom-fonts.scss");
  await writeFile(
    cssPath,
    `/* Generated by scripts/patch-excalidraw-fonts.mjs. Uses local system fonts only. */\n${cssBlocks.join("\n\n")}\n`,
  );

  const indexScssPath = path.join(root, "excalidraw-app/index.scss");
  let indexScss = await readFile(indexScssPath, "utf8");
  const importLine = '@import "./custom-fonts.scss";';
  if (!indexScss.includes(importLine)) {
    indexScss = `${importLine}\n${indexScss}`;
    await writeFile(indexScssPath, indexScss);
  }
}

function insertObjectBlock(text, objectName, marker, body) {
  const clean = removeMarkerBlock(text, marker);
  const close = findObjectClose(clean, objectName);
  const block = `\n  // BEGIN ${marker}\n${body}\n  // END ${marker}\n`;
  return `${clean.slice(0, close)}${block}${clean.slice(close)}`;
}

function findObjectClose(text, declName) {
  const start = text.indexOf(declName);
  if (start === -1) {
    throw new Error(`${declName} was not found`);
  }
  const openBrace = text.indexOf("{", start);
  if (openBrace === -1) {
    throw new Error(`opening brace for ${declName} was not found`);
  }
  let depth = 0;
  let i = openBrace;
  while (i < text.length) {
    const ch = text[i];
    if (ch === '"' || ch === "'" || ch === "`") {
      i = skipStringLiteral(text, i, ch);
      continue;
    }
    if (ch === "/" && text[i + 1] === "/") {
      const nl = text.indexOf("\n", i);
      i = nl === -1 ? text.length : nl + 1;
      continue;
    }
    if (ch === "/" && text[i + 1] === "*") {
      const end = text.indexOf("*/", i + 2);
      i = end === -1 ? text.length : end + 2;
      continue;
    }
    if (ch === "{") {
      depth += 1;
    } else if (ch === "}") {
      depth -= 1;
      if (depth === 0) {
        return i;
      }
    }
    i += 1;
  }
  throw new Error(`matching close brace for ${declName} was not found`);
}

function skipStringLiteral(text, start, quote) {
  let i = start + 1;
  while (i < text.length) {
    const ch = text[i];
    if (ch === "\\") {
      i += 2;
      continue;
    }
    if (quote === "`" && ch === "$" && text[i + 1] === "{") {
      let depth = 1;
      i += 2;
      while (i < text.length && depth > 0) {
        if (text[i] === "{") {
          depth += 1;
        } else if (text[i] === "}") {
          depth -= 1;
        }
        i += 1;
      }
      continue;
    }
    if (ch === quote) {
      return i + 1;
    }
    i += 1;
  }
  return text.length;
}

async function readExistingFontFamilyKeys(root) {
  const constantsPath = path.join(root, "packages/common/src/constants.ts");
  let text = await readFile(constantsPath, "utf8");
  text = removeMarkerBlock(text, "PROJECT_CUSTOM_FONTS");
  return extractObjectKeys(text, "export const FONT_FAMILY");
}

function extractObjectKeys(text, declName) {
  const declStart = text.indexOf(declName);
  if (declStart === -1) {
    return new Set();
  }
  const openBrace = text.indexOf("{", declStart);
  const close = findObjectClose(text, declName);
  const body = text.slice(openBrace + 1, close);
  const keys = new Set();
  for (const rawLine of body.split("\n")) {
    const line = rawLine.replace(/\/\/.*$/, "");
    const match = line.match(/^\s*("[^"]+"|'[^']+'|[A-Za-z_$][\w$]*)\s*:/);
    if (match) {
      keys.add(match[1].replace(/^["']|["']$/g, ""));
    }
  }
  return keys;
}

function insertBeforeDefaultCase(text, marker, body) {
  const clean = removeMarkerBlock(text, marker);
  const functionStart = clean.indexOf("getGenericFontFamilyFallback");
  if (functionStart === -1) {
    throw new Error("getGenericFontFamilyFallback was not found");
  }
  const defaultCase = clean.indexOf("    default:", functionStart);
  if (defaultCase === -1) {
    throw new Error("default fallback case was not found");
  }
  const block = `\n    // BEGIN ${marker}\n${body}\n    // END ${marker}\n`;
  return `${clean.slice(0, defaultCase)}${block}${clean.slice(defaultCase)}`;
}

function removeMarkerBlock(text, marker) {
  const pattern = new RegExp(
    `\\n?\\s*// BEGIN ${marker}[\\s\\S]*?// END ${marker}\\n?`,
    "g",
  );
  return text.replace(pattern, "\n");
}

function quoteCss(value) {
  return `"${String(value).replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

function validateManifest(value) {
  if (!Number.isInteger(value.uiBaseId) || value.uiBaseId < 1001) {
    throw new Error("manifest.uiBaseId must be an integer greater than 1000");
  }
  if (value.downloadSources) {
    throw new Error("manifest.downloadSources is not allowed; custom fonts must use local system fonts only");
  }
  if (!Array.isArray(value.fonts) || value.fonts.length === 0) {
    throw new Error("manifest.fonts must be a non-empty array");
  }
  const ids = new Set();
  const labels = new Set();
  for (const font of value.fonts) {
    if (!font.id || !/^[a-z0-9-]+$/.test(font.id)) {
      throw new Error(`invalid font id: ${font.id}`);
    }
    if (!font.label) {
      throw new Error(`font ${font.id} is missing label`);
    }
    if (!font.localOnly) {
      throw new Error(`font ${font.id} must be localOnly`);
    }
    if (font.source || font.assetPatterns) {
      throw new Error(`font ${font.id} must not reference downloadable assets`);
    }
    if (!Array.isArray(font.localNames) || font.localNames.length === 0) {
      throw new Error(`font ${font.id} needs localNames`);
    }
    if (!["sans-serif", "serif", "monospace"].includes(font.fallback)) {
      throw new Error(`font ${font.id} has invalid fallback: ${font.fallback}`);
    }
    if (ids.has(font.id)) {
      throw new Error(`duplicate font id: ${font.id}`);
    }
    if (labels.has(font.label)) {
      throw new Error(`duplicate font label: ${font.label}`);
    }
    ids.add(font.id);
    labels.add(font.label);
  }
}

function parseArgs(argv) {
  const parsed = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--help" || arg === "-h") {
      parsed.help = true;
    } else if (arg.startsWith("--")) {
      parsed[arg.slice(2)] = argv[index + 1];
      index += 1;
    }
  }
  return parsed;
}

function printHelp() {
  console.log(`Usage: node scripts/patch-excalidraw-fonts.mjs --upstream build/excalidraw --manifest config/custom-fonts.json

Patches a cloned Excalidraw checkout with custom font constants, metadata, and local-font-only CSS.

Options:
  --upstream <path>      Cloned Excalidraw repository path.
  --manifest <path>      Font manifest path.
  --report <path>        Optional JSON report path.
  --help                 Show this help.`);
}